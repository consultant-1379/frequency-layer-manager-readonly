package com.ericsson.oss.services.sonom.flm.service.api.settings;

import java.io.Serializable;
import java.util.Objects;

/**
 * Class to store and represent Group Information.
 */
public class Group implements Serializable {
    private static final long serialVersionUID = 6446959306494624472L;
    protected String name;

    public Group(final String name) {
        this.name = name == null ? "" : name;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        final Group group = (Group) o;
        return name.equals(group.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name);
    }

    @Override
    public String toString() {
        return String.format("%s:: {name: '%s'}",
                getClass().getSimpleName(), name);
    }
}
